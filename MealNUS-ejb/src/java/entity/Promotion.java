package entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;
import java.util.UUID;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.validation.constraints.NotNull;

@Entity
public class Promotion implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long promotionId;
    //Promotion code is autogenerated by the UUID Class
    @Column(nullable = false)
    @NotNull
    private String promotionCode;

    @Column(nullable = false)
    @NotNull
    private String promotionName;

    @Column(nullable = false)
    @NotNull
    private BigDecimal discount;

    //Future scalability in mind
    private String picture;

    @Temporal(TemporalType.DATE)
    @Column(nullable = false)
    @NotNull
    private Date startDate;

    @Temporal(TemporalType.DATE)
    @Column(nullable = false)
    @NotNull
    private Date endDate;

    @Column(nullable = false)
    @NotNull
    private Boolean isApplied;

    @Column(nullable = false)
    @NotNull
    private String categoryName;

    //Promotion code is automatically generated when either constructor is called
    public Promotion() {
        this.promotionCode = UUID.randomUUID().toString().substring(0, 8);
        this.isApplied = false;
    }

    public Promotion(String promotionName, String picture, Date startDate, Date endDate, BigDecimal discount, String categoryName) {
        this();
        this.promotionName = promotionName;
        this.picture = picture;
        this.startDate = startDate;
        this.endDate = endDate;
        this.discount = discount;
        this.categoryName = categoryName;
    }

    public Long getPromotionId() {
        return promotionId;
    }

    public void setPromotionId(Long promotionId) {
        this.promotionId = promotionId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (promotionId != null ? promotionId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the promotionId fields are not set
        if (!(object instanceof Promotion)) {
            return false;
        }
        Promotion other = (Promotion) object;
        if ((this.promotionId == null && other.promotionId != null) || (this.promotionId != null && !this.promotionId.equals(other.promotionId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entity.Promotion[ id=" + promotionId + " ]";
    }

    public String getPromotionName() {
        return promotionName;
    }

    public void setPromotionName(String promotionName) {
        this.promotionName = promotionName;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    public String getPromotionCode() {
        return promotionCode;
    }

    public void setPromotionCode(String promotionCode) {
        this.promotionCode = promotionCode;
    }

    public BigDecimal getDiscount() {
        return discount;
    }

    /**
     * @param discount the discount to set
     */
    public void setDiscount(BigDecimal discount) {
        this.discount = discount;
    }

    /**
     * @return the isApplied
     */
    public Boolean getIsApplied() {
        return isApplied;
    }

    /**
     * @param isApplied the isApplied to set
     */
    public void setIsApplied(Boolean isApplied) {
        this.isApplied = isApplied;
    }

    /**
     * @return the categoryName
     */
    public String getCategoryName() {
        return categoryName;
    }

    /**
     * @param categoryName the categoryName to set
     */
    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    /**
     * @return the isApplied
     */
}
